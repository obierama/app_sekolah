    private void tampil_data() {
        Object[]baris = {"ID.Pembayaran", "ID.Penilaian", "ID.Pendaftaran", "Nama", "Total Pembayaran"};
        tabmode = new DefaultTableModel(null, baris);
        jTable1.setModel(tabmode);
        String sql = "select * from tb_pembayaran order by id_pembayaran asc";
        try {
            Connection konek = new koneksi().getConnection();
            Statement stat = konek.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String id_pembayaran = hasil.getString("id_pembayaran");
                String id_pendaftaran = hasil.getString("id_pendaftaran");
                String id_penilaian = hasil.getString("id_penilaian");
                String nama = hasil.getString("nama");
                String total_pembayaran = hasil.getString("total_pembayaran");
                String[]data ={id_pembayaran, id_penilaian, id_pendaftaran, nama, total_pembayaran};
                tabmode.addRow(data);
            }
            konek.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Menampilkan data gagal", "informasi", JOptionPane.INFORMATION_MESSAGE);
            
        }
    }
